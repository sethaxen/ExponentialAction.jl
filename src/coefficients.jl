function coefficients(tol::T, m_max::Int) where {T<:Real}
    if 2 * tol ≥ eps(Float32)
        return convert(Vector{T}, view(FLOAT32_COEFFICIENTS, 1:m_max))
    elseif 2 * tol ≥ eps(Float64)
        return convert(Vector{T}, view(FLOAT64_COEFFICIENTS, 1:m_max))
    else
        throw(
            DomainError(
                tol,
                "Tolerance $tol with max degree $m_max not supported. Try increasing the tolerance to eps(Float64)/2.",
            ),
        )
    end
end
